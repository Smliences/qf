package com.sml.pojo;

public class Group {
	private int id;
	private String name;
	private int size;
	private User user;
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public int getSize() {
		return size;
	}
	public void setSize(int size) {
		this.size = size;
	}
	
	public User getUser() {
		return user;
	}
	public void setUser(User user) {
		this.user = user;
	}
	@Override
	public String toString() {
		return "Group [id=" + id + ", name=" + name + ", size=" + size + ", user=" + user + "]";
	}
	
	
	
}
